
"""slice in list"""
num_list = [1, 3, 4, 6, 9, 7]
print(num_list[1:4])
print(num_list[::-1])
print(num_list[-1:-4:-2])

"""slice in tuple"""
num_tuple = (1, 4, 8, 7, 3)
print(sorted(num_tuple))
print(num_tuple[1:3])
print(num_tuple[::-1])
print(num_tuple[-1:-4:-1])

